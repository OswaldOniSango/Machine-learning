#Escalares
#No tienen dimensiones, numeros simples, se denotan en minusculas ej: x
#debe ser tipado, como todos los demas tensores, ej: int, float32.

#asignar un escalar
x = 25
print(x)
#saber el tipo especifico
print(type(x))

#asigno otro escalar
y = 3
print(y)

#hacemos una suma
py_suma = x + y
print(py_suma)

#asignamos un decimal
x_float = 25.0
print(x_float)

#al sumar un decimal y un entero, nos da un decimal
float_sum = x_float + y
print(float_sum)



